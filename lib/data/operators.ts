export const allOperators: Operator[] = [...governmentOperators, ...privateOperators]

export function getOperatorById(id: string): Operator | undefined {
  return allOperators.find(op => op.id === id)
}

export function getOperatorsByState(state: string): Operator[] {
  return governmentOperators.filter(op => op.state === state)
}

export function getOperatorsByType(type: 'government' | 'private'): Operator[] {
  return type === 'government' ? governmentOperators : privateOperators
}
